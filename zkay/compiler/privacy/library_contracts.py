import os
from textwrap import dedent

from zkay.config import cfg
from zkay.utils.helpers import read_file


def get_verify_libs_code() -> str:
    """Return all verification contract libraries combined into single string"""
    # The bn256g2 library is in separate file because it is licensed under different terms than zkay
    bn256_lib = read_file(os.path.join(os.path.dirname(os.path.realpath(__file__)), 'bn256g2.sol'))
    return f'pragma solidity ^0.5.0;\n\n{bn256_lib}\n\n{pairing_lib}'


bn128_scalar_field = 21888242871839275222246405745257275088548364400416034343698204186575808495617
"""The field prime used by the zk-snark elliptic curve"""

bn128_scalar_field_bits = bn128_scalar_field.bit_length() - 1
"""Integers of at most this many bits can be represented using field values"""


def get_pki_contract() -> str:
    """Contract of the public key infrastructure used for asymmetric cryptography"""
    return dedent(f'''\
    pragma solidity ^0.5;

    contract {cfg.pki_contract_name} {{
        mapping(address => uint[{cfg.key_len}]) pks;
        mapping(address => bool) hasAnnounced;

        function announcePk(uint[{cfg.key_len}] calldata pk) external {{
            pks[msg.sender] = pk;
            hasAnnounced[msg.sender] = true;
        }}

        function getPk(address a) public view returns(uint[{cfg.key_len}] memory) {{
            require(hasAnnounced[a]);
            return pks[a];
        }}
    }}
    ''')


